package com.springmvc.base.vo;

public class RequestVO {
	private String token;
	private String age;
	
	public void setToken(String token) {
		this.token = token;
	}
	
	public void setAge(String age) {
		this.age = age;
	}
	
	public String getToken() {
		return this.token;
	}
	
	public String getAge() {
		return this.age;
	}

}
